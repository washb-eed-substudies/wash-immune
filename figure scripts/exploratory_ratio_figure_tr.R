clean_lab <- function(d){
  d <- gsub("IL","IL-",d)
  d <- gsub("IFN-g","IFN-\u03b3",d)
  d <- gsub("TNF-a","TNF-\u03b1",d)
  d <- gsub("IL-1b","IL-1\u03b2",d)
  d <- gsub("ratio_pro_il10","Pro\\/IL-10",d)
  d <- gsub("ratio_th1_il10","Th1\\/IL-10",d)
  d <- gsub("ratio_th2_il10","Th2\\/IL-10",d)
  d <- gsub("\nratio","",d)
  return(d)
}

